package game.items;

public class Weapon extends Item {
	
	protected int damage;
	protected int range;
	
	public Weapon(int id, String name, String description){
		super(id, name, description);
		damage = 10;
		range = 1;
	}
	
	public int getDamage(){
		return damage;
	}
	
	public int getRange(){
		return range;
	}

}